let glossaryDiv = document.getElementById("glossary");
let searchInput = document.getElementById("search");
let currentPage = 1;
const itemsPerPage = 5; // Quantidade de itens por página
let itemsPerPageSelect = document.getElementById("itemsPerPage");

function renderGlossary() {
  glossaryDiv.innerHTML = "";
  let searchValue = searchInput.value.toLowerCase();

  // Se a pesquisa estiver vazia, não exibe nenhum termo
  if (searchValue === "") {
    return;
  }

  let matchingTerms = [];
  for (let term in glossary) {
    if (term.toLowerCase().includes(searchValue)) {
      matchingTerms.push(term);
    }
  }

  // Calcula o número total de páginas
  let totalPages = Math.ceil(matchingTerms.length / itemsPerPage);

  // Exibe apenas os termos da página atual
  let start = (currentPage - 1) * itemsPerPage;
  let end = start + itemsPerPage;
  let termsToDisplay = matchingTerms.slice(start, end);

  for (let term of termsToDisplay) {
    let termDiv = document.createElement("div");
    termDiv.className =
      "p-4 rounded-lg shadow-xl bg-white bg-opacity-25 backdrop-blur text-white";

    let termName = document.createElement("div");
    termName.textContent = term;
    termName.className = "text-lg font-semibold mb-2";
    termDiv.appendChild(termName);

    let termDescription = document.createElement("div");
    termDescription.textContent = glossary[term];
    termDescription.className = "text-gray-300";
    termDiv.appendChild(termDescription);

    // Adiciona a imagem correspondente
    if (term in glossaryImages) {
      let termImage = document.createElement("img");
      termImage.src = glossaryImages[term];
      termImage.className = "mt-2"; // Adiciona uma margem no topo
      termDiv.appendChild(termImage);
    }

    glossaryDiv.appendChild(termDiv);
  }

  // Renderiza os botões de paginação
  let paginationDiv = document.getElementById("pagination");
  paginationDiv.innerHTML = "";
  for (let i = 1; i <= totalPages; i++) {
    let pageButton = document.createElement("button");
    pageButton.textContent = i;
    pageButton.className = "px-3 py-1 bg-white text-black rounded shadow";
    if (i === currentPage) {
      pageButton.className += " bg-blue-500 text-white";
    }
    pageButton.addEventListener("click", function () {
      currentPage = i;
      renderGlossary();
    });
    paginationDiv.appendChild(pageButton);
  }
}

searchInput.addEventListener("input", function () {
  currentPage = 1; // Reseta a página atual quando a pesquisa muda
  renderGlossary();
});
